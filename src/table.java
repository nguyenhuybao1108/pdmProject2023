    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try {
            PreparedStatement stmt = Connectionsql.getConnection().prepareStatement("declare @randomstaffID INT\n"
                    + "select @randomstaffID = Staff_id\n"
                    + "from Staff\n"
                    + "order by NEWID()\n"
                    + "insert into Tables(table_Status,Staff_id,slot) values ('A',@randomstaffID,?)");
            
        stmt.setString(1, jTextField2.getText());

            stmt.execute();
            PreparedStatement stmt1 = Connectionsql.getConnection().prepareStatement(
                    "SELECT * FROM Tables"
            );
            ResultSet rs = stmt1.executeQuery();

            StringBuilder results = new StringBuilder();
            ResultSetMetaData metaData = rs.getMetaData();
            int numberOfColumns = metaData.getColumnCount();
            for (int i = 1; i <= numberOfColumns; i++) {
                results.append(metaData.getColumnName(i)).append("\t");
            }
            results.append("\n");
            // meta date
            while (rs.next()) {
                for (int i = 1; i <= numberOfColumns; i++) {
                    results.append(rs.getObject(i)).append("\t");
                }
                results.append("\n");
            }
            jTextArea1.setText(results.toString());
        } catch (SQLException e) {
            jTextArea1.setText(e.getMessage());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

